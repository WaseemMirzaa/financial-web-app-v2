export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() =>
    typeof window !== 'undefined' ? getStoredLocale() : 'en'
  );
  const [isInitialized, setIsInitialized] = useState(false);

  // Set document attributes and mark initialized (useLayoutEffect so ready before paint)
  useLayoutEffect(() => {
    const storedLocale = getStoredLocale();
    setLocaleState(storedLocale);
    if (typeof window !== 'undefined') {
      try {
        localStorage.setItem('locale', storedLocale);
        document.documentElement.setAttribute('dir', storedLocale === 'ar' ? 'rtl' : 'ltr');
        document.documentElement.setAttribute('lang', storedLocale);
      } catch (_) {}
    }
    setIsInitialized(true);
  }, []);

  useLayoutEffect(() => {
    if (isInitialized) {
      document.documentElement.setAttribute('dir', locale === 'ar' ? 'rtl' : 'ltr');
      document.documentElement.setAttribute('lang', locale);
    }
  }, [locale, isInitialized]);

  const setLocale = (newLocale: Locale) => {
    setLocaleState(newLocale);
    if (typeof window !== 'undefined') {
      localStorage.setItem('locale', newLocale);
      document.documentElement.setAttribute('dir', newLocale === 'ar' ? 'rtl' : 'ltr');
      document.documentElement.setAttribute('lang', newLocale);
      const bridge = (window as unknown as { FlutterAppBridge?: { setLocale?: (l: string) => void } }).FlutterAppBridge;
      if (bridge?.setLocale) bridge.setLocale(newLocale);
    }
  };

  const t = (key: string, params?: Record<string, string | number>): string => {
    const safeLocale = (locale === 'ar' ? 'ar' : 'en') as Locale;
    const otherLocale = safeLocale === 'en' ? 'ar' : 'en';
    let text = (translations[safeLocale] && translations[safeLocale][key]) || (translations[otherLocale] && translations[otherLocale][key]) || key;
    if (params && text) {
      Object.keys(params).forEach((paramKey) => {
        const value = params[paramKey];
        text = text.replace(new RegExp('\\{' + paramKey + '\\}', 'g'), String(value));
      });
    }
    return text;
  };

  return (
    <LocaleContext.Provider value={{ locale, setLocale, t, isInitialized }}>
      {children}
    </LocaleContext.Provider>
  );
}

export function useLocale() {
  const context = useContext(LocaleContext);
  if (context === undefined) {
    throw new Error('useLocale must be used within a LocaleProvider');
  }
  return context;
}
